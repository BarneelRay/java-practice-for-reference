package com.barneel.ps5;

//Pattering printing program.
public class ps5_q1
{
    public static void main(String[] args)
    {
        for(int i=4 ;i>0;i--)
        {
            for(int j =0; j<i ;j++)
            {
                System.out.print("*");
            }
            System.out.print("\n");
        }
    }
}
